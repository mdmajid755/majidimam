# Answer 1 : 'print_list_values' is the function to take a list as an argument and prints each value in the list.
print("Solution 1: print_list_values is the function to print out the values in a list that is passed to it.")
# by using example :
def print_list_values(my_list):
        for item in my_list:
                print(item)
my_list = [1, 2, 3, 4, 5]
print_list_values(my_list)

